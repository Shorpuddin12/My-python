print("Comparison operators")
name=20
name1=30
print(name==name1) #equal to
print(name!=name1) #not equal to    
print(name>name1) #greater than
print(name<name1) #less than
print(name>=name1) #greater than or equal to
print(name<=name1) #less than or equal to
"Logical operators"
x=True
y=False 
print(x and y) #and operator
print(x or y) #or operator
print(not x) #not operator
"Identity operators"
x=True
y=False
print(x is y) #is operator
print(x is not y) #is not operator
print("Membership operators")
x=[1,2,3,4,5]
print(1 in x) #in operator
print(6  not in x) #not in operator
print("Bitwise operators")
x=10 #1010 in binary
y=4  #0100 in binary    
